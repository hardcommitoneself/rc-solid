export type ExperienceData = {
    level: number;
    percentage: number;
    color: string;
    max: number;
}

export function experienceToLevel(experience: number): ExperienceData {
    if (experience === 0) {
        return { ...data[0], max: data[1].experience, percentage: 0, color: '#263238' };
    }

    for (var i = 0, len = data.length; i < len; i++) {
        let max = 0;
        if (i + 1 < len) {
            max = data[i + 1].experience;
        }

        let percentage = ((experience - data[i].experience) * 100) / (max - data[i].experience);

        if (experience >= data[i].experience && experience < max) {
            let colorIndex = Math.floor(data[i].level / 5);
            return { ...data[i], max, percentage, color: colors[colorIndex] };
        }
        if (i + 1 == len) {
            let colorIndex = Math.floor(data[i].level / 5);
            return { ...data[i], max, percentage, color: colors[colorIndex] };
        }
    }

    return { ...data[0], max: data[1].experience, percentage: 0, color: '#263238' };
}

